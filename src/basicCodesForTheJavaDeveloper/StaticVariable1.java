package basicCodesForTheJavaDeveloper;

public class StaticVariable1 {
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static String str;
	static boolean bl;
	static char c;
	
	public static void main(String[] args) {
		System.out.println(StaticVariable1.b);
		System.out.println(StaticVariable1.s);
		System.out.println(StaticVariable1.i);
		System.out.println(StaticVariable1.l);
		System.out.println(StaticVariable1.f);
		System.out.println(StaticVariable1.d);
		System.out.println(StaticVariable1.str);
		System.out.println(StaticVariable1.c);
		System.out.println(StaticVariable1.bl);
		
	}

}
