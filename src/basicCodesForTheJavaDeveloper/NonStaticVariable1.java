package basicCodesForTheJavaDeveloper;

public class NonStaticVariable1 {
	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	char c;
	boolean bl;
	String str;
	public static void main(String[] args) {
		NonStaticVariable1 a1 = new NonStaticVariable1();
		System.out.println(a1.b);
		System.out.println(a1.s);
		System.out.println(a1.i);
		System.out.println(a1.l);
		System.out.println(a1.f);
		System.out.println(a1.d);
		System.out.println(a1.c);
		System.out.println(a1.bl);
		System.out.println(a1.str);
		
	}

}
